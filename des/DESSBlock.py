import Hexadecimal

class DESSBlock:
    """
    represents the 8 S-blocks of DES

    For more information:
    http://page.math.tu-berlin.de/~kant/teaching/hess/krypto-ws2006/des.htm
    """
    def __init__(self, s_number = None):
        self.s_number = s_number
        self.validate()
        self.derived_data()

    def validate(self):
        if self.s_number is None:
            raise ValueError("s number is not optional")
        assert(self.s_number >= 1 and self.s_number <= 8)

    def derived_data(self):
        self.lookup_array = self.ALL_S_BLOCKS[self.s_number]
        pass

    def s_transform(self, input):
        assert(len(input) == 6)
        for b in input:
            assert(type(b) is bool)
        row = []
        row.append(input[0])
        row.append(input[5])
        col = input[1:5]
        assert(len(row) == 2)
        assert(len(col) == 4)

        rowint = self.TWO_BIT_TO_INT[tuple(row)]
        colstring = Hexadecimal.Hexadecimal().bit_tuple_to_hex_string(tuple(col))
        assert(len(colstring) == 1)
        colint = Hexadecimal.Hexadecimal().HEX_DIGIT_TO_INT[colstring]

        hexint = self.lookup_array[rowint][colint]
        assert(hexint >= 0 and hexint <= 15)

        hexstr = Hexadecimal.Hexadecimal().INT_TO_HEX_DIGIT[hexint]
        bittuple = Hexadecimal.Hexadecimal().hex_string_to_bit_tuple(hexstr)
        assert(len(bittuple) == 4)

        return bittuple

